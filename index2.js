var Opr = prompt("Operator");

var num1 = parseFloat(prompt("number1: "));
var num2 = parseFloat(prompt("number2: "));


if (Opr == "+") 
{
     console.log(num1 + num2); 
}
else if (Opr == "-") 
{
    console.log(num1 - num2); 
}
else if (Opr == "*") 
{
    console.log(num1 * num2); 
}
else if (Opr == "/") 
{
   console.log(num1 / num2); 
}
